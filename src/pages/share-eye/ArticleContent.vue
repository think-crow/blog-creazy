        世上还有哪个词比“财务自由”更吸引人的吗？恐怕是没有了。当然，圣贤除外，但圣贤毕竟是少数。我不是圣人，所以我也一样爱财，只不过在目前的工作状态下我并不贪财而已。我对财富的渴望并不在于财富本身，而在于重分配权。换句话说，我并没多少需要满足的个人物欲，我有兴趣的只是将财富按照我的意愿重分配给他人。本质上这其实是自私的想法（比如我认为索罗斯做慈善完全是自私的行为，其敛财手段太卑劣）。
